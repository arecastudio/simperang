public class DataPosisi {
    private String id,nama,ket,id_divisi,nama_divisi;

    public DataPosisi() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }

    public String getId_divisi() {
        return id_divisi;
    }

    public void setId_divisi(String id_divisi) {
        this.id_divisi = id_divisi;
    }

    public String getNama_divisi() {
        return nama_divisi;
    }

    public void setNama_divisi(String nama_divisi) {
        this.nama_divisi = nama_divisi;
    }
}
